#o python possui seta operadores arimetricas que são utilizados para realizar operações matematicas como adição subtração e mutiplicação. são elas

#  +   Adição             soma de dois valores                                                                            x  +  y
#  -   subitração         subtrai o valor da esquerda pra direita                                                         x  -  y
#  *   multiplicação      multiplica os dois valores                                                                      x  *  y
#  /   divisão            divide o valor da esquerda para a direita. O resultado sempre retorna como float.               x  /  y
#  %   modulo             resto da divisão do valor da esquerda pelo da direita. o resultado sempre retorna int.          x  %  y
# / /  divisão inteira    do valor da esquerda pelo valor da direita. o resultado sempre é int.                           x / / y
# * *  exponenciação      realiza a potenciaçlização do valor da esquerda elevando ele ao valor da direita.               x * * y


